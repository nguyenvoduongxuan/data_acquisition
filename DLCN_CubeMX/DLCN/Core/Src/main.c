/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_Task1_ADC */
void Task1_ADC(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 100);
		TX_Buffer[0] = Process_value(HAL_ADC_GetValue(&hadc1) * 5000/0xFFF);
		HAL_ADC_Stop(&hadc1);
		HAL_ADC_Start(&hadc2);
		HAL_ADC_PollForConversion(&hadc2, 100);
		TX_Buffer[1] = Process_value(HAL_ADC_GetValue(&hadc2) * 5000/0xFFF);
		HAL_ADC_Stop(&hadc2);
		HAL_ADC_Start(&hadc3);
		HAL_ADC_PollForConversion(&hadc3, 100);
		TX_Buffer[2] = Process_value(HAL_ADC_GetValue(&hadc3) * 5000/0xFFF);
		HAL_ADC_Stop(&hadc3);
		TX_Buffer[3] = '\r';
		TX_Buffer[4] = '\n';
    osDelay(100);
  }
  /* USER CODE END 5 */
}

