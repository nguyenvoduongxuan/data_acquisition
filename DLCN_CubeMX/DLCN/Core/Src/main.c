/**
* @brief Function implementing the myTask02 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_Task2_TX */
void Task2_TX(void const * argument)
{
  /* USER CODE BEGIN Task2_TX */
  /* Infinite loop */
  for(;;)
  {
		osSemaphoreWait(USART_SEMAHandle, osWaitForever);
		HAL_UART_Transmit_DMA(&huart3, (uint8_t *)TX_Buffer, sizeof(TX_Buffer));
		osSemaphoreRelease(USART_SEMAHandle);
		osDelay(1000);
  }
  /* USER CODE END Task2_TX */
}

/* USER CODE BEGIN Header_Task3_RX */
/**
* @brief Function implementing the myTask03 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_Task3_RX */
void Task3_RX(void const * argument)
{
  /* USER CODE BEGIN Task3_RX */
  /* Infinite loop */
  for(;;)
  {
		osSemaphoreWait(USART_SEMAHandle, osWaitForever);
		HAL_UART_Receive_DMA(&huart3, (uint8_t *)RX_Buffer, sizeof(RX_Buffer));
		if (RX_Buffer[1] == '1') HAL_GPIO_WritePin(GPIOB,GPIO_PIN_6,GPIO_PIN_SET);
		else HAL_GPIO_WritePin(GPIOB,GPIO_PIN_6,GPIO_PIN_RESET);
		if (RX_Buffer[3] == '1') HAL_GPIO_WritePin(GPIOB,GPIO_PIN_7,GPIO_PIN_SET);
		else HAL_GPIO_WritePin(GPIOB,GPIO_PIN_7,GPIO_PIN_RESET);
		if (RX_Buffer[5] == '1') HAL_GPIO_WritePin(GPIOB,GPIO_PIN_4,GPIO_PIN_SET);
		else HAL_GPIO_WritePin(GPIOB,GPIO_PIN_4,GPIO_PIN_RESET);
		if (RX_Buffer[7] == '1') HAL_GPIO_WritePin(GPIOB,GPIO_PIN_5,GPIO_PIN_SET);
		else HAL_GPIO_WritePin(GPIOB,GPIO_PIN_5,GPIO_PIN_RESET);
		osSemaphoreRelease(USART_SEMAHandle);
    osDelay(100);
  }
  /* USER CODE END Task3_RX */
}

